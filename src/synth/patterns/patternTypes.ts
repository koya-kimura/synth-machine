/**
 * パターンイベントの型定義
 * 
 * 1つのパターンは複数のイベントで構成されます。
 * 各イベントは特定のビート（タイミング）でどのプリセットを鳴らすかを定義します。
 */

/**
 * パターン内の1つのイベント
 */
export interface PatternEvent {
    /** 
     * どの拍で鳴らすか（0.0～7.999...）
     * 小数点対応: 例えば1.5は1.5拍目、6.25は6.25拍目
     */
    beat: number;

    /** 
     * どのプリセットを鳴らすか（0～31）
     */
    presetIndex: number;
}

/**
 * 1つのパターン定義
 * 
 * パターンは8拍ループで、複数のイベントを持つことができます。
 */
export interface Pattern {
    /** パターン名（デバッグ・UI表示用） */
    name: string;

    /** イベントのリスト */
    events: PatternEvent[];
}
